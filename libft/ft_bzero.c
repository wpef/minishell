#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*ss;

	i = 0;
	ss = (char *)s;
	if (n != 0)
	{
		while (i < n)
		{
			if (ss[i])
				ss[i] = '\0';
			i++;
		}
	}
}
